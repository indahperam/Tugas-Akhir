     */
    public function index(Request $request)
    {
        $cari = $request->cari ?: null;
        $page = $request->showItem ?: 5;
        $user = User::paginate($page)
            ->withQueryString();
        return inertia()->render('pengaturan/users', [
            'user' => $user,
            'search' => $cari,
            'showItem' => $page,
        ]);
    }

